// What is the difference between OOP and POP? 

/*
    OOP
    Definition :- OOP stands for Object Oriented Programing.
    Approach :- OOP follows bottom up approach.
    Division :- A program is divided to objects and their interactions.
    Inheritance supported :- Inheritance is supported.
    Access control :- Access control is supported via access modifiers.
*/

/*
    POP
    Definition :- POP stands for Procedural Oriented Programming.
    Approach :- POP follows top down approach.
    Division :- A program is divided into funtions and they interacts.
    Inheritance supported :- Inheritance is not supported.
    Access control
*/

